#[test]
fn private_runtime_target_is_rejected() {
    let project = TestDir::new();
    let input = project.0.join("main.rua");
    fs::write(
        &input,
        r#"
        #[targets(function)]
        #[retention(runtime)]
        annotation Route();
        #[Route]
        fn private_handler() {}
        "#,
    )
    .unwrap();
    let error = ruac::compile_path(&input).unwrap_err();
    assert!(error.contains("requires a public target"));
}

#[test]
fn metadata_command_exports_filtered_resolved_toml() {
    let project = TestDir::new();
    let input = project.0.join("main.rua");
    fs::write(&input, source()).unwrap();

    let metadata = ruac::compile_path_metadata(&input, Some("Route")).unwrap();
    assert!(metadata.contains("version = 1"));
    assert!(metadata.contains("name = \"Route\""));
    assert!(metadata.contains("annotation = \"Route\""));
    assert!(metadata.contains("target = \"users\""));
    assert!(metadata.contains("method = \"GET\""));

    let output = Command::new(env!("CARGO_BIN_EXE_ruac"))
        .args([
            "metadata",
            "main.rua",
            "--annotation",
            "Route",
            "--format",
            "toml",
        ])
        .current_dir(&project.0)
        .output()
        .unwrap();
    assert!(
        output.status.success(),
        "{}",
        String::from_utf8_lossy(&output.stderr)
    );
    assert_eq!(String::from_utf8(output.stdout).unwrap(), metadata);
}
